package com.example.rentalcar.object;

public class fuel {
    int f_id;
    String fiel;

    public fuel(int f_id, String fiel) {
        this.f_id = f_id;
        this.fiel = fiel;
    }

    public int getF_id() {
        return f_id;
    }

    public String getFiel() {
        return fiel;
    }
}
